import java.util.*;

public class If {
    public static void main (String[] args){
    Scanner sc=new Scanner(System.in);
    System.out.println("Entar value:-");
    int a=sc.nextInt();
    if (a>10){
      System.out.println("a is +10");
    }
    if (a<10){
        System.out.println("a is -10");
    }
    if (a==10){
        System.out.println("a is 10");
    }
    }
}
